package org.example.view.button;

import javax.swing.*;

/**
 * The Button class is a custom JButton with a text label.
 * It extends the JButton class from the Swing library.
 */
public class Button extends JButton {

    /**
     * Constructs a Button with the specified text label.
     *
     * @param text the text to be displayed on the button
     */
    public Button(String text) {
        super(text);
    }
}
